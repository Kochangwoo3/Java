package classpart;

public class _01_method2 {

	//메서드 정의
	public static void printStar() {
		System.out.println("###############################");
	}
	public static void printLine() {
		System.out.println("-------------------------------");
	}
	
	public static void main(String[] args) {
		// 메서드
		printStar();  // 메서드 호출
		printLine();
		printStar();  // 메서드 호출
		printLine();
		printLine();;
		printStar();  // 메서드 호출
		printStar();  // 메서드 호출
		printLine();
		printLine();
		printLine();
		printStar();  // 메서드 호출
		printStar();  // 메서드 호출
		printLine();
	}

}
