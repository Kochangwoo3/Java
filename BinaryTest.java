package chapter2;

public class BinaryTest {
	public static void main(String[] args) {
		// 0B : 2진수, 0: 8진수, 0X : 16진수
		int num = 10;
		int bNum = 0B1010;
		int oNum = 012;
		int hNum = 0XA;
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}
}